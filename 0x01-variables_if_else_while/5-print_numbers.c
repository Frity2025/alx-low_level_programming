#include <stdio.h>

/**
 * main - program that prints all single digit numbers
 *
 * Description: using the main function
 * this program that prints all single digit numbers of base 10 starting from 0
 * Return: 0
 */
int main(void)
{
int j;

for (j = 0; j < 10; j++)
{
printf("%i", j);
}
printf("\n");
return (0);
}

